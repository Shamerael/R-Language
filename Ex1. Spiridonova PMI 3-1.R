# 1.3 Создайте матрицу Х и вектор У. а затем найдите вектор параметров парной линейной регрессии а с точностью до одного знака после запятой.
X <- matrix(c(-21, 11, -5, 8, -4, 1, 1, 9, 0, 4), 5, 2) #создали матрицу Х
X #вывели матрицу Х
Y <- c(-163, 99, 0, 71, -12) #создали ветор У
Y #вывели вектор У
A <- round(solve(t(X) %*% X) %*% t(X) %*% Y, 1) #нашли вектор параметров парной линейной регрессии
A #вывод результата


