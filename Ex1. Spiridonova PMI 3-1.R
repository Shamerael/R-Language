# 1.2 Вычислить для фрейма df и округлить реультат до 2-ух знаков после запятой. Используйте векторизацию.
n <- 26  #номер варианта
df <- data.frame(a = c(1, 4, 6, 7), b = c(-3, -9, 0, -11))  #задаем фрейм
df #вывод результата
